#include <stdio.h>

int main() {
    char a = 97;
    printf("%c",a); //a
    printf("%u",a); //97
    a ++;
    printf("%c",a); //b
    printf("%u",a); //98

    char b = 'a';
    printf("%c",b); //a
    printf("%u",b); //97
    b++;
    printf("%c",b); //b
    printf("%u",b); //98



}